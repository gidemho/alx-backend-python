#!/usr/bin/env python3
"""

This module provides a function to compute the floor of a float number.
"""

import math


def floor(n: float) -> int:
    """

    Returns the floor of a float number.


    parameters:
    n (float): The number to compute the floor of.


    Returns:
    int: The floor of the number.

    """
    return math.floor(n)
