#!/usr/bin/env python3
"""
This module provides a function to convert a float number to its string rep.
"""


def to_str(n: float) -> str:
    """
    Returns the string rep of a float number.

    Parameters:
    n (float): The number to convert to a string.

    Returns:
    str: The string rep of the number.
    """
    return str(n)
